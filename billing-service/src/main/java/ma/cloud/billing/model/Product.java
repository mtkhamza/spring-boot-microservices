package ma.cloud.billing.model;



public class Product {
    private Long id;
    private String name;
    private double price;
    private double quantity;

    public Long getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public double getPrice() {
        return price;
    }

    public double getQuantity() {
        return quantity;
    }
}






